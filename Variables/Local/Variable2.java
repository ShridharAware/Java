class Variable2{
        byte a1;
        short a2;
        int a3;
        long a4;
        float a5;
        double a6;
        char a7;
        boolean a8;
        String a9;
    public static void main(String[] args){
        Variable2 var = new Variable2();
        System.out.println("Byte default value is "+var.a1);
        System.out.println("Short default value is "+var.a2);
        System.out.println("Int default value is "+var.a3);
        System.out.println("Long default value is "+var.a4);
        System.out.println("Float default value is "+var.a5);
        System.out.println("Double default value is "+var.a6);
        System.out.println("Char default value is "+var.a7);
        System.out.println("Boolean default value is "+var.a8);
        System.out.println("String default value is "+var.a9);
    }
}